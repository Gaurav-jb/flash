package com.gaurav.telusko;

public class OddEven {
	
	public static void main(String[] args) {
		
		int n= 0;
		
		if(n==0)
		{
			System.out.println("Nothing...");
		}
		else if(n%2==0)
		{	
			System.out.println("Even");
		}
		else
		{	
			System.out.println("Odd");
		}	
	}

}
